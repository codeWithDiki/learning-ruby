# define a numbers data type on ruby

a = 10
b = 20

# Remember with string ? for numbers we don't use " as a wrapper, you can directly type numbers like 10 or etc.
# let's go using basic mathematical operations (+, -, /, *, %)

#start with Addition (+)
puts a + b # result is 30

#then with Subtraction
puts b - a # result is 10

# Multiplication
puts a * b # result is 300

# Division
puts b / a # result is 2

# Modulus
puts b % a # result is 0